CREATE TABLE fornecedores (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    nome_fantasia VARCHAR(150) NOT NULL,
    razao_social VARCHAR(200) DEFAULT NULL,
    cnpj_cpf VARCHAR(30) DEFAULT NULL,
    categoria_ramo VARCHAR(150) DEFAULT NULL,
    vendedor VARCHAR(150) DEFAULT NULL,
    whatsapp VARCHAR(60) DEFAULT NULL,
    telefone_fixo VARCHAR(60) DEFAULT NULL,
    email_pedidos VARCHAR(150) DEFAULT NULL,
    site VARCHAR(255) DEFAULT NULL,
    cep VARCHAR(9) DEFAULT NULL,
    logradouro VARCHAR(255) DEFAULT NULL,
    numero VARCHAR(20) DEFAULT NULL,
    complemento VARCHAR(120) DEFAULT NULL,
    bairro VARCHAR(120) DEFAULT NULL,
    cidade VARCHAR(120) DEFAULT NULL,
    estado_uf CHAR(2) DEFAULT NULL,
    endereco TEXT DEFAULT NULL,
    prazo_entrega_medio VARCHAR(120) DEFAULT NULL,
    pedido_minimo VARCHAR(120) DEFAULT NULL,
    condicoes_pagamento TEXT DEFAULT NULL,
    dados_bancarios TEXT DEFAULT NULL,
    chave_pix VARCHAR(180) DEFAULT NULL,
    qualidade TINYINT UNSIGNED DEFAULT NULL,
    observacoes_gerais TEXT DEFAULT NULL,
    ultima_atualizacao TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    usuario_id INT UNSIGNED NOT NULL,
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
